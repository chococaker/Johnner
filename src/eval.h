    float evaluate(const Board& board) {
        float eval = 0;
        uint8_t activeColor = board.state.activeColor;
        uint8_t opp = OPPOSITE_SIDE(activeColor);

        float pst = 0;

        // material
        for (int i = 0; i < 6; i++) {
            if (i != 0) {
                eval += STATIC_PIECE_VALUES[i] * countOnes(board.bitboards[activeColor][i]);
                eval -= STATIC_PIECE_VALUES[i] * countOnes(board.bitboards[opp][i]);
            }

            iterateIndices(board.bitboards[activeColor][i],
                [i, &board, &pst, activeColor](uint8_t index){
                size_t pstIdx = (board.state.activeColor == SIDE_WHITE) ? 63 - index : index;
                pst += PIECE_SQUARE_TABLES[i][pstIdx];
            });

            iterateIndices(board.bitboards[opp][i],
                [i, &board, &pst, opp](uint8_t index) {
                size_t pstIdx = (board.state.activeColor == SIDE_WHITE) ? index : 63 - index;
                pst -= PIECE_SQUARE_TABLES[i][pstIdx];
            });
        }

        return eval + (pst * .01f);
    }
